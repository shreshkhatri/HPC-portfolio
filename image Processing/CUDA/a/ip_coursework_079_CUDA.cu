int time_difference(struct timespec *start, struct timespec *finish,
                    long long int *difference) {
  long long int ds =  finish->tv_sec - start->tv_sec; 
  long long int dn =  finish->tv_nsec - start->tv_nsec; 

  if(dn < 0 ) {
    ds--;
    dn += 1000000000; 
  } 
  *difference = ds * 1000000000 + dn;
  return !(*difference > 0);
}



 __global__ void detect_edges(unsigned char *input_image_pixel, unsigned char *output_image_pixel) {

  //calculating address for pixel to be processed 
  int i = (blockIdx.x * blockDim.x) + threadIdx.x;

  int x, y; 
  int b, d, f, h;
  int r; 
    
    y = i / WIDTH;
    x = i - (WIDTH * y);

    if (x == 0 || y == 0 || x == WIDTH - 1 || y == HEIGHT - 1) {
      output_image_pixel[i] = 0;
    } else {
      b = i + WIDTH;
      d = i - 1;
      f = i + 1;
      h = i - WIDTH;
      
      //applyinf filter matrix for filtering 
      r = (input_image_pixel[i] * 4) + (input_image_pixel[b] * -1) + (input_image_pixel[d] * -1) + (input_image_pixel[f] * -1)
          + (input_image_pixel[h] * -1);

      if (r > 0) { // if the result is positive this is an edge pixel
        output_image_pixel[i] = 255;
      } else {
        output_image_pixel[i] = 0;
      }
    }
  }




void tidy_and_exit() {
  exit(0);
}



void sigint_callback(int signal_number){
  printf("\nInterrupt from keyboard\n");
  tidy_and_exit();
}


static void display() {
  glClear(GL_COLOR_BUFFER_BIT);
  glRasterPos4i(-1, -1, 0, 1);
  glDrawPixels(WIDTH, HEIGHT, GL_LUMINANCE, GL_UNSIGNED_BYTE, image);
  glRasterPos4i(0, -1, 0, 1);
  glDrawPixels(WIDTH, HEIGHT, GL_LUMINANCE, GL_UNSIGNED_BYTE, results);
  glFlush();
}


static void key_pressed(unsigned char key, int x, int y) {
  switch(key){
    case 27:
      tidy_and_exit();
      break;
    default: 
      printf("\nPress escape to exit\n");
      break;
  }
}



int main(int argc, char **argv) {
   
  //defining block and thread dimension
  dim3 block_Dim(WIDTH,1,1),thread_Dim(HEIGHT,1,1);
 
  unsigned char *d_results;
  unsigned char *d_image;
  
  
//device memory allocation for device variables using error checking

  if(cudaMalloc((void**)&d_image, sizeof(unsigned char) * PIXEL_SIZE))
    {
     printf("%s\n", cudaGetErrorString(cudaGetLastError()));
     exit(1);
    }
  
  if(cudaMalloc((void**)&d_results, sizeof(unsigned char) * PIXEL_SIZE))
  {
   printf("%s\n", cudaGetErrorString(cudaGetLastError()));
   exit(1);
  }

 
  //copying input image pixel data from host to device
  if(cudaMemcpy(d_image, &image, sizeof(unsigned char) * PIXEL_SIZE, cudaMemcpyHostToDevice))
  {
    printf("%s\n", cudaGetErrorString(cudaGetLastError()));
   exit(1);
  }
   
  signal(SIGINT, sigint_callback);
 

  struct timespec start, finish;   
  long long int time_elapsed;

  clock_gettime(CLOCK_MONOTONIC, &start);
  detect_edges<<<block_Dim,thread_Dim>>>(d_image, d_results);
  
  //barier point for threads
  cudaThreadSynchronize();

  //copying output image pixel data from device to host
  cudaMemcpy(&results, d_results, sizeof(unsigned char) * PIXEL_SIZE, cudaMemcpyDeviceToHost);
 


  clock_gettime(CLOCK_MONOTONIC, &finish);
  time_difference(&start, &finish, &time_elapsed);
  printf("Time elapsed was %lldns or %0.9lfs\n", time_elapsed, 
         (time_elapsed/1.0e9));
  
  //freeing of allocated device memory
  cudaFree(d_image);
  cudaFree(d_results);

  glutInit(&argc, argv);
  glutInitWindowSize(WIDTH * 2,HEIGHT);
  glutInitDisplayMode(GLUT_SINGLE | GLUT_LUMINANCE);
      
  glutCreateWindow("6CS005 Image Progessing Courework using CUDA");
  glutDisplayFunc(display);
  glutKeyboardFunc(key_pressed);
  glClearColor(0.0, 1.0, 0.0, 1.0); 

  glutMainLoop(); 

  tidy_and_exit();
  
  return 0;
}


